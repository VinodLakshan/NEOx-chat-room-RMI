    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Registry createRegistry = null;
        if (jButton1.getText().equalsIgnoreCase("Start Server")) {
            jButton1.setText("Stop Server");
            lblStatus.setText("Server is Starting....!!");
            try {
                createRegistry = LocateRegistry.createRegistry(1616);
                createRegistry.rebind("NEOxServer", new ChatControllerImpl());
            } catch (RemoteException ex) {
                Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
            }

        } else {
            jButton1.setText("Start Server");
            lblStatus.setText("Server is Stopped....!!");

            
            try {
                createRegistry.unbind("NEOxServer");
                UnicastRemoteObject.unexportObject(createRegistry, true);
            } catch (NoSuchObjectException ex) {
                Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
            } catch (RemoteException ex) {
                Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
            } catch (NotBoundException ex) {
                Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

